      ,[NotificationTypeID]
      ,[ReportingOrgID]
      ,[ReportingOrgName]
      ,[ReportingOrgAddress]
      ,[PatronID]
      ,[PatronName]
      ,[EmailAddress]
      ,[PatronAddress]
      ,[PhoneVoice]
      ,[EmailFormatID]
      ,[ItemRecordID]
      ,[DueDate]
      ,[BrowseTitle]
      ,[ItemFormat]
      ,[ItemAssignedBranch]
      ,[Renewals]
      ,[ReportingOrgPhone]
      ,[AdminLanguageID]
      ,[DeliveryOptionID]
      ,[IsAdditionalTxt]
      ,[AutoRenewal]
      ,[CreationDate]
  FROM [Results].[Polaris].[CircReminders]
    WHERE CreationDate >= DATEADD(day, -6, GETDATE())
  ORDER BY CreationDate DESC